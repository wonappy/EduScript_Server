# 발화 요약용 프롬프트

def summarize_lecture_prompt():
    prompt = """
        이 발화 내용은 대학 강의에서 교수님이 말하신 걸 텍스트로 변환한 녹음본 입니다. 이 발화 내용을 다음 유의사항을 참고하여 요약본으로 만들어주세요.

유의사항

-줄 글 문단으로 써줄 것.

-이모티콘 사용 금지

-수업 내용과 관련없는 부분은 뺄 것

-일정과 같은 주요 사항 (교수님 공지사항, 과제 제출 기한 등) 내용은 제외
    """
    return prompt

def summarize_meeting_prompt():
    """회의 정제용 프롬프트"""
    prompt = """
    이 발화 내용은 회의에서 녹음된 텍스트를 변환한 것입니다. 다음 유의사항과 형식에 맞추어 회의록을 작성해주세요.

[유의사항]
- 회의 발언자들의 발언 내용을 요약 및 정리하되, 발언자의 이름은 표기하지 않습니다
- 회의 중 논의된 각 안건별로 핵심 내용을 분리하여 정리합니다
- 각 안건에서 나온 주요 논점들을 요약하여 기록해 주세요
- 마지막에는 모든 안건의 내용을 종합해 하나의 최종 결론을 제시해 주세요
- 특수문자, 이모지, 마크다운 문법은 사용하지 말고 일반 텍스트로만 작성해주세요

[출력 형식]
회의록: (회의 주제)
일시: (날짜)
주제: (전체 주제)

안건 1: (소주제명)
- 주요 논의 내용 1
- 주요 논의 내용 2
- 주요 논의 내용 3
결론: (해당 안건의 간단한 결론)

안건 2: (소주제명)
- 주요 논의 내용 1
- 주요 논의 내용 2
- 주요 논의 내용 3
결론: (해당 안건의 간단한 결론)

최종 결론: (전체 안건을 종합한 결론)

[형식 예시]
회의록: 2025년 1분기 프로젝트 진행 회의
일시: 2025년 1월 20일
주제: 신규 프로젝트 기획 및 일정 조율

안건 1: 프로젝트 범위 및 목표 설정
- 목표 고객층을 20대에서 30대로 확장하기로 결정
- 프로젝트 예산을 기존 계획보다 20% 증액
- 개발 기간을 3개월에서 4개월로 연장
결론: 확장된 범위와 충분한 예산으로 프로젝트 품질 향상 기대

안건 2: 팀 구성 및 역할 분담
- 개발팀 3명, 디자인팀 2명, 마케팅팀 2명으로 구성
- 주간 회의를 매주 금요일 오후 2시로 고정
- 각 팀별 중간 보고를 격주로 실시
결론: 명확한 역할 분담으로 효율적인 프로젝트 진행 가능

최종 결론: 확장된 프로젝트 범위와 체계적인 팀 구성을 통해 성공적인 프로젝트 완료를 목표로 4개월간 진행하기로 최종 결정

위 형식에 따라 아래 텍스트를 정리해 주세요:
    """
    return prompt

