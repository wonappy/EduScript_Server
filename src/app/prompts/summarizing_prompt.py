# 발화 요약용 프롬프트

def summarize_lecture_prompt():
    prompt = """
        이 발화 내용은 대학 강의에서 교수님이 말하신 걸 텍스트로 변환한 녹음본 입니다. 이 발화 내용을 다음 유의사항을 참고하여 요약본으로 만들어주세요.

유의사항

-줄 글 문단으로 써줄 것.

-이모티콘 사용 금지

-수업 내용과 관련없는 부분은 뺄 것

-일정과 같은 주요 사항 (교수님 공지사항, 과제 제출 기한 등) 내용은 제외
    """
    return prompt

def summarize_meeting_prompt():
    """회의 정제용 프롬프트"""
    prompt = """
    이 발화 내용은 회의에서 녹음된 텍스트를 변환한 것입니다. 다음 유의사항과 형식에 맞추어 회의록을 작성해주세요.

✅ 유의사항:
- 회의 발언자들의 발언 내용을 요약 및 정리하되, 발언자의 이름은 표기하지 않습니다.
- 회의 중 논의된 각 안건별로 핵심 내용을 분리하여 정리합니다.
- 각 안건에서 나온 주요 논점들을 요약하여 기록해 주세요.
- 마지막에는 모든 안건의 내용을 종합해 하나의 '최종 결론'을 제시해 주세요.

✅ 형식:
1. 회의 제목 / 일시 / 주제
2. 안건 1: (주제)
   - 주요 논의 내용 bullet point 정리
   - 해당 안건의 간단한 결론
3. 안건 2: (주제)
   - 주요 논의 내용 bullet point 정리
   - 해당 안건의 간단한 결론
4. …
5. ✅ 최종 결론: 전체 안건을 종합한 결론 제시

형식 예시는 다음과 같습니다:

* 회의록: [회의 주제]  
일시: [날짜]  
주제: [전체 주제]

---

### 안건 1: [소주제명]  
- ...
- ...
(간단한 결론)

---

### 안건 2: [소주제명]  
- ...
- ...
(간단한 결론)

---

✅ 최종 결론:  
(전체 안건을 종합한 결론 제시)

---

해당 회의록 형식에 따라 아래 텍스트를 정리해 주세요:

    """
    return prompt

